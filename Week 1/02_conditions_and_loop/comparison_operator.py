# 6 types of comparison operator

# less then :  <

print (12 < 35)

# greater then : >

print (35 > 12)

# greater then or equal >=

print (12 >= 12)
print (13 >= 12)

# less then or equal <=

print (12 <= 12)
print (11 <= 12)

# equal ==

my_money = 45
your_money = 45
her_money = 65

print (my_money == your_money)

# not equal !=

print (my_money != her_money )